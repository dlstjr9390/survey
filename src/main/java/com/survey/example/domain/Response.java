package com.survey.example.domain;

public class Response {
	private int sSIdx;
	private int sIdx;
	private int qIdx;
	private String response;
	private String uId;
	private int count;
	
	public int getsSIdx() {
		return sSIdx;
	}
	public void setsSIdx(int sSIdx) {
		this.sSIdx = sSIdx;
	}
	public int getsIdx() {
		return sIdx;
	}
	public void setsIdx(int sIdx) {
		this.sIdx = sIdx;
	}
	public int getqIdx() {
		return qIdx;
	}
	public void setqIdx(int qIdx) {
		this.qIdx = qIdx;
	}
	public String getResponse() {
		return response;
	}
	public void setResponse(String response) {
		this.response = response;
	}
	public String getuId() {
		return uId;
	}
	public void setuId(String uId) {
		this.uId = uId;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
	
}
